package Modelos.PraEmpleados;


public class clsPraEmpleados {
    
    public int id_praempleado;
    public String nombre;
    public String contraseña;

    public int getId_praempleado() {
        return id_praempleado;
    }
    
    public void setId_praempleado(int id_praempleado) {
        this.id_praempleado = id_praempleado;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getContraseña() {
        return contraseña;
    }

    public void setContraseña(String contraseña) {
        this.contraseña = contraseña;
    }
    
    
    
}
